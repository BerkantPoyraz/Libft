#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*head;

	head = (t_list *)malloc(sizeof(*head));
	if (head == NULL)
		return (0);
	head->content = content;
	head->next = NULL;
	return (head);
}
