#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t			x;
	unsigned char	*ptr;
	unsigned char	*ptr2;

	ptr = (unsigned char *)dest;
	ptr2 = (unsigned char *)src;
	if (!dest && !src)
		return (0);
	x = 0;
	while (x < n)
	{
		ptr[x] = ptr2[x];
			x++;
	}
	return (dest);
}
